A papadum (also called papad) is a thin, crisp, round flatbread from India. It is typically based on a seasoned dough usually made from peeled black gram flour (urad flour), either fried or cooked with dry heat (usually flipping it over an open flame). Flours made from other sources such as lentils, chickpeas, rice, tapioca, certain millets or potato can be used.<br><br>

Papadums are typically served as an accompaniment to a meal in India, Pakistan, Bangladesh, Nepal and Sri Lanka or as an appetizer or snack, sometimes with toppings such as chopped onions, chopped carrots, chutneys or other dips, and condiments. In Pakistan, these are made of rice and often used as a snack or meal accompaniment. In certain parts of India, papadums which have been dried, but not cooked, are used in curries and vegetable dishes. Papadums have been a part of the cuisine of the Indian subcontinent for generations[citation needed] and are an intrinsic part of everyday meals.<br><br>

Papadums are called appalam in Tamil Nadu, pappadam in Kerala, appadam in Andhra/Telangana, happala in Karnataka, papad in Punjab and Gujarat, and pampada in Odisha .[citation needed] They are mainly made in Punjab, Rajasthan, Maharastra, and Gujarat in the north. In the south, Kerala, Madurai district in Tamil Nadu, Kanchipuram, and Chennai are major manufacturers.<br><br>


<b>Etymology</b><br><br>
Papadum is derived from the Sanskrit word parpaṭa (पर्पट), meaning a flattened disc.[citation needed]<br><br>

