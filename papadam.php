Different types of papadums are sold at stores.<br><br>
Papadum recipes vary from region to region and from family to family. They are typically made from flour or paste derived from either lentils, chickpeas, black gram, rice, or potato.<br><br>

Salt and peanut oil are added to make a dough, which can be flavored with seasonings such as chili, cumin, garlic, or black pepper. Sometimes, baking soda or slaked lime is also added. The dough is shaped into a thin, round flatbread and then dried (traditionally in the sun), and can be cooked by deep frying, roasting over an open flame, toasting, or microwaving, depending on the desired texture.<br><br>

Bikaner is the hub of chickpea and green gram papadam manufacturing. Potato papadam is made in Varanasi. Most sweet- and snack-selling national companies are also involved in this business. In the north, papads made in the city of Amritsar and its surrounds (Amritsari Papar Warian) are especially famous. The Lijjat Papad company (Shri Mahila Griha Udyog Lijjat Papad), headquartered in Mumbai, is a major manufacturer, and is often cited as an exemplar of the women's empowerment movement in India, as it is run entirely by women.<br><br>

In most Indian restaurants around the world, they are served as an appetizer with dips, which often include mango chutney, lime pickle, onion chutney, and raita. Masala Papad with sev, onion, tomato, coriander leaves toppings, is one of the best selling appetizers in India.<br><br>

<b>Ingredients and preparation</b><br><br>
Papadum can be prepared from different ingredients and methods. Arguably, the most popular recipe uses flour ground from hulled split black gram[1] mixed with black pepper, salt, and a small amount of vegetable oil and a food-grade alkali, and the mixture is kneaded. A well-kneaded dough is then flattened into very thin rounds and then dried and stored for later preparation and consumption. It may also contain rice, Jackfruit, Sabudana, etc., as main ingredients. Cracked black pepper, red chili powder, Asafoetida, or cumin or sesame seeds are often used as flavoring agents. Papad or Happala is also made from Rice flakes, Ragi and Horsegram also.<br><br>

<b>Business</b><br><br>
Papadum is often associated with the empowerment of women in India.[3] Many individual and organized businesses run by women produce papad, pickles, and other snacks. This provides them regular income from minimal financial investments. Shri Mahila Griha Udyog Lijjat Papad is an organization (owned and run solely by women) which produces large quantities of papadums on the open market, which started as a small business in the late 1950s,[4][5] with an annual income in 2005 of about ₹650 crore (US$91 million). However, with the recent growth of modern trade in India and the growing consumer awareness, other brands have been gaining in popularity within this category.<br><br>

<b>Spelling and pronunciation</b><br><br>
Some divergence of transliteration may be noted in the third consonant in the Hindi/Urdu word pāpaṛ. The sound is the retroflex flap [ɽ], which is written in Hindi with the Devanagari letter ड़, and in Urdu script with the Perso-Arabic letter ڑ. Although in IAST the Hindi letter ड़ is transliterated as <ṛ>, popular or nonstandard transliterations of Hindi use <d> for this sound, because etymologically it derives from ड /ɖ/. The occurrence of this consonant in the word pāpaṛ has given rise to two alternative spellings in English: papad, which reflects its etymology, and papar (anglicized as "popper"), which reflects its phonology.</p>
