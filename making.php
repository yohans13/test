	<h2>New Nile Products Making</h2><br>
	<p> 

<b>Mixing and kneading</b><br><br>

This is the process in which a mixing machine stirs up wheat flour and the mixing water. Normally 0.3-0.4kg of the mixing water at a temperature of 20-30˚C is kneaded into the flour for 15-20 minutes. This process gives the dough textiform tissue that generates noodle’s elasticity.<br><br>

<b>Forming noodle belt</b><br><br>

The dough is put through two rotating rollers, which compounds two noodle belts into one single belt. This process helps distribute ingredients evenly. Occasionally the dough is left for a certain period time to mature.<br><br>

<b>Rolling</b><br><br>

The 10mm-thick noodle belt is pressed with pressing rollers, and it is flattened to a thickness of approximately 1mm. The noodles are passed through more than four pairs of rollers and become thinner gradually. This process strengthens the textiform tissue and gives elasticity to the noodles.<br><br>


<b>Slitteing</b><br><br>

After the rolling process, noodles are put into the slitter, where a rolling blade slits the noodle belt into thin noodles.　Most of the instant noodles are wavy. Being pressed up and down gently, the noodles cut out by the slitter become wavy. The wavy-shape gives space in between noodles, which prevent the noodles from sticking together.<br><br>

<b>Steaming</b><br><br>

Most instant noodles are steamed for 1-5 minutes at 100˚C to pregelatinize the starch of the noodles (pregelatinization).<br><br>

<b>Feeder (Molding)</b><br><br>

Noodles are cut into 40-70cm long and are put into a round- or square- shaped metal mold serving.<br><br>

 <b>Dipping Bath</b><br><br>
The steamed noodles are dipped in seasoning.<br>

